    public void atualizaCliente(){
        List<Cliente> lista = new ArrayList();
        DefaultTableModel tbCliente = (DefaultTableModel) tblUtil.getModel();
        lista = controlCliente.pegaTudoCliente();
        tbCliente.setRowCount(0);
        for (Cliente cliente : lista) {
            Object[] dadosBebida = new Object[]{ cliente.idCliente, cliente.nomeCliente, cliente.cpfCliente, cliente.usuarioCliente, cliente.senhaCliente, cliente.emailCliente};
            tbCliente.addRow(dadosBebida);
        }
        tbCliente.fireTableDataChanged();
    }
    
    public void atualizaFuncionario(){
        List<Funcionario> lista = new ArrayList();
        DefaultTableModel tbCliente = (DefaultTableModel) tblUtil.getModel();
        lista = controlFuncionario.pegaTudoFuncionario();
        tbCliente.setRowCount(0);
        for (Funcionario funcionario : lista) {
            Object[] dadosBebida = new Object[]{ funcionario.idFunc, funcionario.nomeFunc, funcionario.cpfFunc, funcionario.usuarioFunc, funcionario.senhaFunc, funcionario.emailFunc};
            tbCliente.addRow(dadosBebida);
        }
        tbCliente.fireTableDataChanged();
    }
    

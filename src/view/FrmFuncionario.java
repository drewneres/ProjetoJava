    private void tblUtilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUtilMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tblUtil.getSelectedRow();
        if(linhaSelecionada != -1){
            Object id = tblUtil.getValueAt(linhaSelecionada, 0);
            txtId.setText(id.toString());
            Object nome = tblUtil.getValueAt(linhaSelecionada, 1);
            txtNome.setText(nome.toString());
            Object cpf = tblUtil.getValueAt(linhaSelecionada, 2);
            txtCpf.setText(cpf.toString());
            Object usuario = tblUtil.getValueAt(linhaSelecionada, 3);
            txtUsuario.setText(usuario.toString());
            Object senha = tblUtil.getValueAt(linhaSelecionada, 4);
            txtSenha.setText(senha.toString());
            Object email = tblUtil.getValueAt(linhaSelecionada, 5);
            txtEmail.setText(email.toString());
        }
    }//GEN-LAST:event_tblUtilMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        if(selecc == "Cliente"){
            clie.cpfCliente = txtCpf.getText();
            clie.emailCliente = txtEmail.getText();
            clie.nomeCliente = txtNome.getText();
            clie.senhaCliente = txtSenha.getText();
            clie.usuarioCliente = txtUsuario.getText();
            controlCliente.cadastroCliente(clie);
        }
        else if(selecc == "Funcionario"){
            func.cpfFunc = txtCpf.getText();
            func.emailFunc = txtEmail.getText();
            func.nomeFunc = txtNome.getText();
            func.senhaFunc = txtSenha.getText();
            func.usuarioFunc = txtUsuario.getText();
            controlFuncionario.cadastroFuncionario(func);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(selecc == "Cliente"){
            clie.cpfCliente = txtCpf.getText();
            clie.emailCliente = txtEmail.getText();
            clie.idCliente = Integer.parseInt(txtId.getText());
            clie.nomeCliente = txtNome.getText();
            clie.senhaCliente = txtSenha.getText();
            clie.usuarioCliente = txtUsuario.getText();
            controlCliente.editarCliente(clie);
            atualizaCliente();
        }
        else if(selecc == "Funcionario"){
            func.cpfFunc = txtCpf.getText();
            func.emailFunc = txtEmail.getText();
            func.idFunc = Integer.parseInt(txtId.getText());
            func.nomeFunc = txtNome.getText();
            func.senhaFunc = txtSenha.getText();
            func.usuarioFunc = txtUsuario.getText();
            controlFuncionario.editarFuncionario(func);
            atualizaFuncionario();
        }
    }//GEN-LAST:event_btnEditarActionPerformed


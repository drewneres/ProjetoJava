    public void atualizarTabela(){
        List<Bebida> lista = new ArrayList();
        DefaultTableModel tbBebida= (DefaultTableModel) tblBebida.getModel();
        lista = control.pegaBebida();
        tbBebida.setRowCount(0);
        for (Bebida bebida : lista) {
            Object[] dadosBebida = new Object[]{ bebida.codigoBebida, bebida.nomeBebida, bebida.tipoBebida, bebida.precoBebida, bebida.qtdeEstoque};
            tbBebida.addRow(dadosBebida);
        }
        tbBebida.fireTableDataChanged();
    }

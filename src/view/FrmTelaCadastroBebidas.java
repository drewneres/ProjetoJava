    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        bb.nomeBebida = txtNomeBebida1.getText();
        String precoo = txtPreco.getText();
        String estoque = txtEstoque.getText();
        String codigo = txtCodBebida.getText();
        bb.tipoBebida = cbxTipoBebida.getSelectedItem().toString();
        if(precoo.equals("") || codigo.equals("") || bb.nomeBebida.equals("") || estoque.equals("") || bb.tipoBebida.equals("Selecionar")){
            lblSup.setText("preencha todos os campos para editar");
            lblSup.setForeground(Color.red);
            lblSup.setVisible(true);
        }else{
        bb.precoBebida = Double.parseDouble(txtPreco.getText());
        bb.qtdeEstoque = Integer.parseInt(txtEstoque.getText());
        bb.codigoBebida = Integer.parseInt(txtCodBebida.getText());
        control.editarBebida(bb);
        atualizarTabela();
        txtCodBebida.setText("");
        txtEstoque.setText("");
        txtFindBId.setText("");
        txtNomeBebida1.setText("");
        txtPreco.setText("");
        cbxTipoBebida.setSelectedItem("Selecionar");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed


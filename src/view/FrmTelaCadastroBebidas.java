    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        try{
            int cod = Integer.parseInt(txtCodBebida.getText());
            if(control.verificarCodBebida(cod)){
                control.excluirBebida(cod);
                lblSup.setText("Bebida deletada com sucesso");
                lblSup.setForeground(Color.blue);
                lblSup.setVisible(true);
            }else{
                lblSup.setText("Codigo bebida não encontrado");
                lblSup.setForeground(Color.red);
                lblSup.setVisible(true);
            }
        }catch(NumberFormatException e){
            System.out.println("numero invalido");
        }
        txtCodBebida.setText("");
        txtEstoque.setText("");
        txtFindBId.setText("");
        txtNomeBebida1.setText("");
        txtPreco.setText("");
        cbxTipoBebida.setSelectedItem("Selecionar");
        atualizarTabela();
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void tblBebidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBebidaMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tblBebida.getSelectedRow();
        if(linhaSelecionada != -1){
            Object codigoBebida = tblBebida.getValueAt(linhaSelecionada, 0);
            txtCodBebida.setText(codigoBebida.toString());
            Object nomeBebida = tblBebida.getValueAt(linhaSelecionada, 1);
            txtNomeBebida1.setText(nomeBebida.toString());
            Object tipoBebida = tblBebida.getValueAt(linhaSelecionada, 2);
            if(tipoBebida.equals("Vodka")){
                cbxTipoBebida.setSelectedItem("Vodka");
            }
            else if(tipoBebida.equals("Gin")){
                cbxTipoBebida.setSelectedItem("Gin");
            }
            else if(tipoBebida.equals("Vinho")){
                cbxTipoBebida.setSelectedItem("Vinho");
            }
            else if(tipoBebida.equals("Whisky")){
                cbxTipoBebida.setSelectedItem("Whisky");
            }
            else if(tipoBebida.equals("Cerveja")){
                cbxTipoBebida.setSelectedItem("Cerveja");
            }
            Object precoBebida = tblBebida.getValueAt(linhaSelecionada, 3);
            txtPreco.setText(precoBebida.toString());
            Object estoque = tblBebida.getValueAt(linhaSelecionada, 4);
            txtEstoque.setText(estoque.toString());
        }
    }//GEN-LAST:event_tblBebidaMouseClicked


    public void atualizarTabela(){
        List<Compras> listaCompra = new ArrayList();
        //List<Bebida> listaBebida = new ArrayList();
        String userPego = frmlogin.pegaUser();
        int idConfirm = controlCliente.pegaIdCliente(userPego);
        System.out.println("" + idConfirm);
        listaCompra = controlC.hist(idConfirm);
        DefaultTableModel tbHistorico = (DefaultTableModel) tblHistorico.getModel();
        tbHistorico.setRowCount(0);
        
        
        //int tamanho = Math.min(listaCompra.size(), listaBebida.size());
        for(Compras compras : listaCompra){
            //Compras compras = listaCompra.get(i);
            //Bebida bebida = listaBebida.get(i);
            List<Bebida> listaBebida = controlB.histCompra(compras.codBebida);
            
            for(Bebida bebida : listaBebida){
                Object[] dadosBebida = new Object[]{ 
                compras.codCompra, 
                bebida.nomeBebida, 
                bebida.tipoBebida, 
                compras.precoBebida, 
                compras.quantidade, 
                compras.diaCompra, 
                compras.total
            };
                tbHistorico.addRow(dadosBebida);
            }
            
        }
        tbHistorico.fireTableDataChanged();
    }
    
    
